// with numbers
const months = 1

switch (months) {
    case 1:
        console.log("jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break;
    default:
        console.log("invalid value");
        break;
}

// with string
const weeks = "monday"

switch (weeks) {
    case "sunday":
        console.log("Sunday");
        break;
    case "monday":
        console.log("Monday");
        break;
    case "tuesday":
        console.log("Tuesday");
        break;
    default:
        console.log("invalid value");
        break;
}